    // Douglas
function AddAcertoDouglas(){

    let inputAcertoDouglas = Number(document.getElementById('iadouglas').value)
    inputAcertoDouglas = parseInt(inputAcertoDouglas + 1)
    document.getElementById('iadouglas').value = inputAcertoDouglas
}

function RemoveAcertoDouglas(){

    let inputAcertoDouglas = Number(document.getElementById('iadouglas').value)
    inputAcertoDouglas = parseInt(inputAcertoDouglas - 1)
    document.getElementById('iadouglas').value = inputAcertoDouglas
}

function AddErroDouglas(){

    let inputErroDouglas = Number(document.getElementById('iedouglas').value)
    inputErroDouglas = parseInt(inputErroDouglas + 1)
    document.getElementById('iedouglas').value = inputErroDouglas
}


